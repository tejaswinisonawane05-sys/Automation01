package CoreJava;

public class PremitiveType {

	public static void main(String[] args) {
		int a = 5;
		double b = 12.65;
		char c = 'T';
		boolean Type = true;
		
		System.out.println("int value " +a);
		System.out.println("double value " +b);
		System.out.println("char value " +c);
		System.out.println("boolean value " +Type);
			
	}

}
