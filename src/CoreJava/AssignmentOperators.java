package CoreJava;

public class AssignmentOperators {

	public static void main(String[] args) {
		int num = 20;
		int expression1 = num+=5;
		int expression2 = num-=3;
		int expression3 = num*=2;
		int expression4 = num/4;
		int expression5 = num%5;
		System.out.println("Adds and assigns "+expression1);
		System.out.println("Subtracts and assigns "+expression2);
		System.out.println(" "+expression3);
		System.out.println(" "+expression4);
		System.out.println(" "+expression5);
		

	}

}
