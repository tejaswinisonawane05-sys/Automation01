package CoreJava;

public class UnaryOperators {

	public static void main(String[] args) {
		int a =5;
		System.out.println(a++); //this print 5
		System.out.println(+a); // this will print 6
		System.out.println(++a); // this will print 7
		System.out.println(a); // this will pint 7 only
		
		System.out.println(a--); //this print 7
		System.out.println(a); // this will print 6
		System.out.println(--a); // this will print 5
		System.out.println(a); // this will pint 5 only


	}

}
